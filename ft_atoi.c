int	ft_atoi(const char *str)
{
	long	r;
	long	n;

	r = 0;
	n = 1;
	while (*str == ' ' || *str == '\n' || *str == '\f' || *str == '\r'
		|| *str == '\t' || *str == '\v')
		str++;
	if (*str == '-')
	{
		n = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str != '\0' && *str >= 47 && *str <= 57)
	{
		r = r * 10 + *str - '0';
		str++;
	}
	return (n * r);
}
